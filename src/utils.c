/* Draw a single character at (x,y), scale = pixel size per font-pixel */
static void draw_char(SDL_Renderer *r, char ch, int x, int y, int scale, SDL_Color c) {
    /* Font covers ASCII 32-126; anything outside is drawn as '?' */
    if (ch < 32 || ch > 126) ch = '?';
    int idx = (int)ch - 32;
    set_color(r, c);
    for (int col = 0; col < 5; col++) {
        Uint8 col_data = font5x7[idx][col];
        for (int row = 0; row < 7; row++) {
            if (col_data & (1 << row)) {
                SDL_Rect px = { x + col*scale, y + row*scale, scale, scale };
                SDL_RenderFillRect(r, &px);
            }
        }
    }
}

/* Draw a null-terminated string */
void draw_text_simple(SDL_Renderer *r, const char *text, int x, int y, int size, SDL_Color c) {
    int cx = x;
    while (*text) {
        draw_char(r, *text, cx, y, size, c);
        cx += 6 * size;
        text++;
    }
}

/* ── Distance squared ─────────────────────────────────────────────── */
float dist2(float x1, float y1, float x2, float y2) {
    float dx = x2-x1, dy = y2-y1;
    return dx*dx + dy*dy;
}

/* ── Logging ───────────────────────────────────────────────────────── */
void log_event(const char *fmt, ...) {
    FILE *f = fopen(LOG_FILE, "a");
    if (!f) return;
    time_t t = time(NULL);
    struct tm *tm_info = localtime(&t);
    char timebuf[32];
    strftime(timebuf, sizeof(timebuf), "%H:%M:%S", tm_info);
    fprintf(f, "[%s] ", timebuf);
    va_list args;
    va_start(args, fmt);
    vfprintf(f, fmt, args);
    va_end(args);
    fprintf(f, "\n");
    fclose(f);
}

