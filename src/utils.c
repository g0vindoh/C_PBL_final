/* ── SDL colour helper ────────────────────────────────────────────── */
static void set_color(SDL_Renderer *r, SDL_Color c) {
    SDL_SetRenderDrawColor(r, c.r, c.g, c.b, c.a);
}

/* ── Filled rectangle ─────────────────────────────────────────────── */
void draw_rect_filled(SDL_Renderer *r, int x, int y, int w, int h, SDL_Color c) {
    set_color(r, c);
    SDL_Rect rect = {x, y, w, h};
    SDL_RenderFillRect(r, &rect);
}

/* ── Outlined rectangle ───────────────────────────────────────────── */
void draw_rect_outline(SDL_Renderer *r, int x, int y, int w, int h, SDL_Color c, int thickness) {
    for (int i = 0; i < thickness; i++) {
        set_color(r, c);
        SDL_Rect rect = {x+i, y+i, w-2*i, h-2*i};
        SDL_RenderDrawRect(r, &rect);
    }
}

/* ── Hollow circle (Midpoint algorithm) ───────────────────────────── */
void draw_circle(SDL_Renderer *r, int cx, int cy, int radius, SDL_Color c) {
    set_color(r, c);
    int x = radius, y = 0, err = 0;
    while (x >= y) {
        SDL_RenderDrawPoint(r, cx+x, cy+y); SDL_RenderDrawPoint(r, cx+y, cy+x);
        SDL_RenderDrawPoint(r, cx-y, cy+x); SDL_RenderDrawPoint(r, cx-x, cy+y);
        SDL_RenderDrawPoint(r, cx-x, cy-y); SDL_RenderDrawPoint(r, cx-y, cy-x);
        SDL_RenderDrawPoint(r, cx+y, cy-x); SDL_RenderDrawPoint(r, cx+x, cy-y);
        y++;
        if (err <= 0) { err += 2*y+1; }
        if (err > 0)  { x--; err -= 2*x+1; }
    }
}

/* ── Filled circle ────────────────────────────────────────────────── */
void draw_circle_filled(SDL_Renderer *r, int cx, int cy, int radius, SDL_Color c) {
    set_color(r, c);
    for (int dy = -radius; dy <= radius; dy++) {
        int dx = (int)sqrtf((float)(radius*radius - dy*dy));
        SDL_RenderDrawLine(r, cx - dx, cy + dy, cx + dx, cy + dy);
    }
}

/* ── Rounded rectangle (approximate) ─────────────────────────────── */
void draw_rounded_rect(SDL_Renderer *r, int x, int y, int w, int h, int rad, SDL_Color c) {
    /* Fill inner cross */
    draw_rect_filled(r, x+rad, y, w-2*rad, h, c);
    draw_rect_filled(r, x, y+rad, w, h-2*rad, c);
    /* Corner circles */
    draw_circle_filled(r, x+rad,     y+rad,     rad, c);
    draw_circle_filled(r, x+w-rad-1, y+rad,     rad, c);
    draw_circle_filled(r, x+rad,     y+h-rad-1, rad, c);
    draw_circle_filled(r, x+w-rad-1, y+h-rad-1, rad, c);
}

/* ── Glow circle (layered semi-transparent circles) ───────────────── */
void draw_glow_circle(SDL_Renderer *r, int cx, int cy, int radius, SDL_Color c, int layers) {
    SDL_SetRenderDrawBlendMode(r, SDL_BLENDMODE_ADD);
    for (int i = layers; i >= 1; i--) {
        SDL_Color gc = c;
        gc.a = (Uint8)(c.a / (i * 2));
        draw_circle_filled(r, cx, cy, radius + i * 4, gc);
    }
    draw_circle_filled(r, cx, cy, radius, c);
    SDL_SetRenderDrawBlendMode(r, SDL_BLENDMODE_NONE);
}

